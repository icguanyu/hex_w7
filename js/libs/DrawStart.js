//星星每次畫10顆,每次位置都不一樣
function drawStar() {
  sgx.save();
  for (var i = 0; i <= 10; i++) {
    var sp = {
      x: Math.random() * ww,
      y: Math.random() * wh,
      s: Math.random() * 5
    };
    sgx.fillStyle = "rgba(255,255,255,.8)";
    sgx.beginPath();
    sgx.arc(sp.x + i, sp.y + i, sp.s, 0, pi2);
    sgx.fill();
    sgx.restore();
  }
}
